/***************************************************************************************************
  * 描述   : 延时函数
  * 参数   : 无
  * 返回   : 无
  * 注意   : 延时函数与MCLK有关，不同频率下的Dealy_Base下头文件有定义
   *************************************************************************************************/
void delay_ms(uint16_t ms)
{
	uint16_t i;
	uint16_t j;
	for(i=0;i<ms;i++)
	{
	   for(j=0;j<Dealy_ms_Base;j++);
	} 
}
void delay_us(uint16_t us)
{
	uint16_t i;
	for(i=0;i<us;i++)
	{
      ;
      #if  Dealy_us_Base>2
           _NOP();_NOP();_NOP();_NOP();
      #endif 
      #if  Dealy_us_Base>4
           _NOP();_NOP();_NOP();_NOP();_NOP();_NOP();_NOP();
      #endif
	} 
}
