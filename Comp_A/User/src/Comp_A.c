/***************************************************************************************************
  * 描述   : 比较器配置
  * 参数   : 无
  * 返回   : 无
  * 注意   : 比较器+端接P1.2,-端接内部参考电压0.25Vcc
  *************************************************************************************************/
void Comp_Init(void)
{
  P1DIR |= BIT3;//P1.3比较输出
  P1SEL |= BIT3;
  P1SEL2|= BIT3;
  CAPD = 0;
  CAPD |= BIT2+BIT4;//通道2，即P1.2比较输入P2CA3+
  CACTL1=CARSEL+CAREF0+CAREF1+CAON;//0.25Vcc比较
  CACTL2=P2CA0+P2CA4+CAF; 
}
